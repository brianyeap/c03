int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	while (s1[i] == s2[i] && s1[i] && i < n - 1)
		i++;
	if (s1[i] == s2[i])
		return (0);
	return (s1[i] - s2[i]);
}
